const mongoose = require("mongoose");

const UserSchemaa = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  userName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: { type: String, required: true },
});

const User = mongoose.model("User", UserSchemaa);
module.exports = User;
